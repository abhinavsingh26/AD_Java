
public class type_cast 
{

	public static void main(String[] args) 
	{
		// Converting data from one type to another type 
		
		int i = 10;
		float f = i;
		System.out.println(f);
		
		float fl = 20.3f;
		double d = fl;
		System.out.println(d);
		
		float f1 = 10.5f;
		int i1 = (int) ( f1);
		System.out.println(i1);
		
	}

}
